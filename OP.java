class OP {
    public static void main(String []args) {
        
        // 10 x (2 ^ 2)
        System.out.println(10 << 2);

        // 10 x (2 ^ 3)
        System.out.println(10 << 3);

        // 32 / (2 ^ 2)
        System.out.println(32 >> 2);

        // 32 / (2 ^ 3)
        System.out.println(32 >> 3);
    }
}